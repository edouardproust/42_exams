#include <unistd.h>



int	main(int ac, char **av)
{
	char	*s;

	if (ac == 2)
	{
		s = av[1];
		while (*s)
		{
			if ('a' <= *s && *s <= 'z')
				*s -= 32;
			else if ('A' <= *s && *s <= 'Z')
				*s += 32;
			write(1, s, 1);
			s++;
		}
	}
	write(1, "\n", 1);
	return (0);
}
